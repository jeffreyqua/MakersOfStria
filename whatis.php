			<p>Our expertise in media production affords collaboration with other makers to effectively tell their story.</p>

			<p>Traditionally, makers may not have an authentic digital presence; web stores focus solely on product retail or cookie cutter labels. 
			The people, craft, and history of the makers is lost - Stria sees these as cornerstones that should be emphasized. Stria campaigns increase exposure to a global audience while still retaining their central values and ideals.</p>
			
			<p>We design micro media campaigns which delve into each craft from the perspective of the makers. These campaigns are made up videos, and interactive tools that develop a digital personas. Stria then drives the media onto the web - leveraged it through existing web platforms (such as Vimeo, Kickstarter, etc) and the Stria website.</p>
		</div>
		
		<div class="right">
			<h2>Mission</h2>
			<p>To offer a raw perspective of makers through their stories by showcasing the beauty of what they do to a broad audience of viewers.</p>
			<h2>Vision</h2>
			<p>To offer a service that encourages makers and craftsmen to display their work online as authentically as they can, retaining the aesthetic feel of their local workshops, but opening their business to a global audience -in turn creating awareness for the Maker Movement.</p>
