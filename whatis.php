			<p>The makers we work with are defined by the care and effort they put into creating their products. This craftsmanship is defined by the passion they dedicate to honing their expertise and developing the skill. The byproduct is a transparency of process and quality embedded within the final product.</p>

			<p>Makers used to be central to most small communities, the cobbler down the street, the foundrymen who made all the horseshoes and metal tools. Craftsmanship was founded on person-to-person relationships, skill and pride in one's craft, and buyers respect.</p>
			
			<p>Today, meeting makers in person is a sought after, but often limited opportunities. The stories they tell, and markings made on each piece are central to their products, their identity, and how they can still touch their audience.</p>
